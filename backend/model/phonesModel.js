import mongoose from "mongoose";
const PhonesSchema = new mongoose.Schema({
  title: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  image: {
    type: String,
    required: true,
  },
  price: {
    type: String,
    required: true,
  },
});

const phoneModel = mongoose.model("phone", PhonesSchema);
export default phoneModel;
